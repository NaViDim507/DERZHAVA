package com.example.derzhava.data

/**
 * Представляет авторизованного пользователя на клиенте. Помимо логина и пароля
 * теперь хранится флаг администратора. Администратор имеет доступ к
 * дополнительным экранам (например, панели управления пользователями) и
 * расширенным операциям. Поле [isAdmin] по умолчанию равно false и
 * устанавливается на основании данных, приходящих с сервера при
 * авторизации/регистрации. Пароль продолжает храниться локально, поскольку
 * PHP‑API принимает только явный пароль при входе.
 */
data class User(
    val rulerName: String,     // Имя правителя
    val countryName: String,   // Название державы
    val password: String,      // Пароль (для простоты без шифрования)
    val isAdmin: Boolean = false // Флаг, указывающий, является ли пользователь администратором
)
